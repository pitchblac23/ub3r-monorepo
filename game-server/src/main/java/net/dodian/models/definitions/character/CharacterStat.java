package net.dodian.models.definitions.character;

public class CharacterStat {
    private int id;
    private int skillId;
    private int characterId;
    private int level;
    private int experience;

    public int getId() {
        return id;
    }

    public int getSkillId() {
        return skillId;
    }

    public int getCharacterId() {
        return characterId;
    }

    public int getLevel() {
        return level;
    }

    public int getExperience() {
        return experience;
    }
}
